# Constant for the YAML file where Aer counts are saved.
AER_SAVE_DIRECTORY = "./saved/jobs/aer_counts.yml"
IBMQ_SAVE_DIRECTORY = "./saved/jobs/ibmq_counts.yml"
IBMQM3_SAVE_DIRECTORY = "./saved/jobs/ibmqM3_counts.yml"

def unsupervised_aer_jobs(model, n_jobs, M):
    """
    Run unsupervised jobs using the Qiskit Aer simulator and store the resulting counts.

    This function reads an existing YAML file (if available) containing previous job counts,
    runs the specified number of jobs using the provided model's circuit on the AerSimulator,
    updates the counts with new job results, and writes them back to the YAML file.

    Parameters:
        model (QiskitModel): The quantum model containing the circuit to run.
        n_jobs (int): The number of jobs to execute.
        M (int): The number of shots for each job.

    Returns:
        list: A list of job IDs generated by the AerSimulator.
    """
    # Attempt to load existing job data; if file not found, use an empty dict.
    try:
        with open(AER_SAVE_DIRECTORY, 'r') as file:
            aer_data = yaml.safe_load(file)
    except FileNotFoundError:
        aer_data = {}
    if aer_data is None:
        aer_data = {}

    job_ids = []
    backend = AerSimulator(method="statevector")

    for _ in range(n_jobs):
        job = backend.run([model.circuit], shots=M)
        job_id = job.job_id()
        job_ids.append(job_id)
        aer_data[job_id] = dict(job.result().get_counts())

    # Write the updated job data back to the YAML file.
    with open(AER_SAVE_DIRECTORY, 'w') as file:
        yaml.dump(aer_data, file)

    return job_ids


